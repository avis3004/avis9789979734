<p><b> 1.1 &nbsp;Salient Features of the Scheme</b></p>
<ol type="1">
<li>	Provision of 100 days of guaranteed wage employment for all registered households of the Village Panchayat.</li>
<li>The Central Government bears 100% wage cost of unskilled manual labour and 75% of the material cost including wages of skilled and semi-skilled workers.</li>
<li>No contractors or machineries are allowed.</li>
<li>The wage and material component ratio has to be maintained at 60:40. In Tamil Nadu, 100% labour intensive works alone are taken up under MGNREGS.</li>
<li>As per the guidelines, the shelf of projects for a Village should be recommended by the Grama Sabha and 50% of the works should be allotted to Village Panchayats for execution.
 In case of Tamil Nadu, only the Village Panchayats execute the works.</li>
<li>Adult members of rural households willing to do unskilled manual work may register in writing or orally, with the Village Panchayat.</li>
<li>The Village Panchayat will issue a job card, bearing the photo and register number, free of cost.</li>
<li>Job card is issued within 15 days of application / oral request.</li>
<li>Job card holders may apply for employment in writing. In such cases, the Village Panchayat will issue a dated receipt which will stand as a guarantee to provide employment within 15 days.</li>
<li>The worksite is ordinarily within a radius of 5 kms of the Village Panchayat and if the distance exceeds 5 kms, additional wage of 10% of existing wage rate is payable to meet the additional transportation and living expenses.</li>
<li>	The wages are paid according to the Minimum Wages Act 1948, for agricultural labourers in the State.</li>
<li>Wages are equal to both men and women.</li>
<li>Disbursement of wages is normally done on weekly basis and should be paid within 15 days.</li>
<li>One third of the beneficiaries should be women.</li>
<li>The  wage  notified   by GOI per person per day is Rs.119/-  till 2011-12. GOI has increased  and  notified   the   wage as Rs.132/-  for  the   year 2012-13.The notified wage rate for 2013-14 is Rs.148/-.</li>
<li>	Work site facilities such as drinking water, first aid kit, shade, etc., are provided.</li>
<li>Grama Sabha conducts the social audit in respect of MGNREGS through Village Social Auditors.</li>
<li>Grievance redressal mechanism is in place to ensure quick response and a Toll free Help line (1299) has also been provided.</li>
<li>	All accounts and records of the Scheme are available for public scrutiny.</li>
<li>Under Section 12 of the MGNREG Act, Government of Tamil Nadu has constituted State Employment Guarantee Council to advise, evaluate and monitor the implementation of the Scheme.
</li></ol>
<p><b>1.2 &nbsp;Permissible Works </b></p>

          <p align="left">The permissible work as per the GOI operational guidelines are as follows:</p>
		  
		  <ol type="a">
		  <b><li>100 % Labour Intensive works</li></b>
		  <ol type="1">
		  <li>Water conservation and water harvesting including contour trenches, contour bunds, boulder checks, gabion structures, underground dykes, earthen dams, stop dams and springshed development;</li>
<li>Drought proofing including afforestation and tree plantation;</li>
<li>Irrigation canals including micro and minor irrigation works;</li>
<li>Provision of irrigation facility, dug out farm pond, horticulture, plantation, farm bunding and land development on land owned by households belonging to the Scheduled Castes and the Scheduled Tribes or Below Poverty Line families or the beneficiaries of land reforms or the beneficiaries under the  Indira Awaas Yojana of the Government of India or that of the small or marginal farmers as defined in the Agriculture Debt Waiver and Debt Relief Scheme, 2008, or the beneficiaries under the Scheduled Tribes and Other Traditional Forest Dwellers (Recognition of Forest Rights) Act, 2006 (2 of 2007).</li>
<li>Renovation of traditional water bodies including desilting of tanks;</li>
<li>Land development;</li>
<li>Flood control and protection works including drainage in water logged areas including deepening and repairing of flood channels;</li>
<li>Rural connectivity to provide all weather access (Earthen Roads); and</li>
<li>Works in coastal areas like belt vegetation, rural drinking water related works such as soak pits and recharge pits.</li>
</ol>
<b><li>Negative List</li></b>
<ol>
<li>Works like boulder, pebble or shrub removal, silt application and similar activities are not permissible as stand alone activities except when they are part of tasks in projects for converting uncultivable and barren / fallow lands into cultivable lands through one time land development and wherever land development is taken up, it may follow an area approach preferably a micro-watershed and not confined to a few individual holdings.</li>
<li>Normal agriculture operations such as land preparation, ploughing, sowing, weed removal, turning the soil, watering, harvesting, pruning and such similar operations are not permissible in MGNREGA.</li>
<li>Agriculture operations of any nature are not permitted to be taken up under MGNREGA for food grain crops, vegetables, floriculture, etc. The material cost for these inputs (seeds, fertilizers, pesticide) would also not be covered under MGNREGA.</li>
<li>Removal of weeds and watering may be considered for payment under MGNREGA only for forestry plantations and horticulture limited for a duration of 2-3 years. It is envisaged that at the end of this period, the horticulture plants would become fruit bearing and thereafter, weeding, watering, etc., would become a part of normal operations. Likewise, at the end of the 2-3 years, it is envisaged that the agro forestry plantation would attain sufficient height and should be able to survive with protection from grazing which can be provided by ditch cum mound bunds along the periphery or other forms of physical structures as given in the MGNREGA Operational Guideline 2013. The State Agriculture / Horticulture/ Forest department would prescribe norms in respect of the duration and persondays for which removal of weeds and watering, etc., for horticulture and forestry may be included as cost of MGNREGA work. However, these norms should be the same as applicable to works taken up under their own departmental programme either from state funds or as part of a Centrally Sponsored Schemes.</li></ol>
</ol>
<b>1.3.2 Permission to take up more than one work in a Village Panchayat </b>
<p>Prior to 15.5.2011, only one worksite was permitted in each Village Panchayat at any given time. This has led to:</p>
<ul>
<li>Huge assemblage of workers at a single worksite.</li>
<li>Difficulties in supervising the workers.</li>
<li>Preventing the workers coming from far off habitations to the worksite.</li>
<li>Practice of the &#146Rotation of Habitations&#146 system which has resulted in the family availing only less number of days of employment on an average as against an entitlement of 100 days.</li>
</ul>
<p>In order to provide more employment opportunities to MGNREGS workers, this Government has committed to take up more number of works in a Village Panchayat based on the number of habitations and number of workers regularly attending the MGNREGS works. Hence, the habitations in a Village Panchayat are grouped into clusters in such a way that whenever the work is being executed in a habitation of a particular cluster, all the workers can come to the worksite from the habitations of that particular cluster. Thus, the number of allowed works in a Village Panchayat is equal to the number of clusters.
This system effected from October, 2011 resulted in:
</p>
<ul>
<li>Drawing more number of workers to the MGNREGS worksite as the worksite is nearer to their habitations.</li>
<li>In comparison with the 26.86 crore mandays generated in 2010-11, it has been increased to 33 crore in 2011-12.  Like wise mandays generation during 2012-13 is 38.99 crore (upto 13.3.2013).</li>
<li>Expenditure per week on payment of wages to the workers has increased from Rs.50 crore to Rs.75 crore during 2011-12 and to Rs.100 crore per week during 2012-13.</li>
<li>Travel Distance of the workers to the worksite has been reduced from 5-6 km to 500 metre to 1km.</li>
<li>Increase in number of families provided with employment over the years.</li>
</ul>
<b>1.3.3 Land Development Activities</b>
<ul>
<li>The MGNREG Act provides for development of lands owned by households belonging to the SC /ST, Small and Marginal farmers or the lands of BPL families and the lands of the beneficiaries of IAY and Land reforms. Such land development activities are being practiced in some States.</li>
<li>With the Government&#146s decision to explore the possibility of extending land development activities including irrigation facilities, horticulture, etc., to the eligible landholders, the following actions were initiated during 2011-12:</li>
<ul type="circle">
<li>Two teams of officials were sent to Andhra Pradesh and Kerala to make a thorough study about their implementation.</li>
<li>Detailed discussions were held with Agriculture and Horticulture Departments.</li>
<li>Time and motion studies were conducted in Erode, Trichy, Virudhunagar and Vellore Districts.</li>
</ul>
<li>Based on the above activities, detailed guidelines have been issued in consultation with Agriculture, Animal Husbandry, Forest and Horticulture Departments vide G.O.(Ms).No.61 RD&PR (CGS-1) Department dated 23.7.2012. Land development works in the lands of individual farmers and afforestation works in common Government lands have been taken up on pilot basis during 2012-13. Land based activities will be scaled up to all the eligible farmers in the year 2013-14.</li>
<li>Detailed guidelines to take up &#148Farm Ponds&#148 under MGNREGS has been issued vide G.O.(Ms).No17 RD&PR(CGS-1) Department, Dated 15.02.2013.</li>
</ul>
<b>1.3.5  Special works for Differently Abled</b>
<ul>
<li>In order to implement the Provisions of the Persons with Disabilities (Equal Opportunities, Protection of Rights and Full Participation) Act, 1995 in MGNREGS, the Government of Tamilnadu has ordered for special provisions for the disabled vide G.O.(Ms).No.52 RD&PR (CGS-1) Department dated 25.6.2012.</li>
</ul>
<b>As per the Government Order, the special activities for disabled persons under MGNREGS are:</b>
<ul>
<li>Waterman/waterwoman at the worksite, who can distribute water to the workers.</li>
<li>Caretaker to look after the children.</li>
<li>Assisting the caretaker. For every 5 children, one additional assistant can be employed.</li>
<li>Assisting the worksite facilitator in premarking. If the No.of workers is above 100, then 1 disabled person shall be permitted to assist the worksite facilitator.</li>
<li>Clearing uprooted jungle from the site (only scrubs, light jungle).</li>
<li>Watering (wetting) the area to be desilted (especially in summers).</li>
<li>Compacting the earth deposited on bund by using spade, solid rods and earth breaking rods.</li>
<li>Benching and levelling the bund.</li>
<li>Sectioning and sloping the bund.</li>
</ul>
<b>Special provisions for doing earth work related activities are:</b>
<ul>
<li>The Differently-abled persons who are able to carry out physical work can be utilized for earth work related activities like jungle clearance, desilting work, planting and refilling and watering.</li>
<li>In the worksite(s), where the number of disabled persons is less in number, then they must be engaged only in the special activities enlisted above.</li>
<li>In the worksite(s), where the number of disabled workers is high, then they can be engaged in earthwork. In such cases, they can be grouped into groups of 5 or 10 and the premarking shall be done only for 50% of quantity earmarked for regular workers.</li>
</ul>
<b>1.3.6 Selection of Works by using Satellite Based Maps></b>
<ul>
<li>This Government has already committed to generate satellite-based maps indicating the current status of water bodies (Tank/Oorani/Supply Channels) in comparison to the original capacity of the water bodies;</li>
<li>Using Topo sheets of 1960s and LISS -IV Indian Remote Sensing (IRS) satellite images, detailed Blockwise maps indicating the original extent of water bodies and the current status have been generated by the Institute of Remote Sensing (IRS), Anna University. Based on the detailed field verification by the Rural Development and Panchayat Raj Department officials, the exact, present field conditions have also been incorporated and digitized. Now, the resource map is available for all Districts and Districtwise, Blockwise and Village Panchayatwise maps can be generated.</li>
<li>As a measure to restore the original capacity of the water bodies, and to increase the water recharge, most of the works based on these satellite maps have been selected during 2012-13</li>
<li>On a pilot basis &#148System Tanks&#148 has been taken up for renovation under MGNREGS including the repair and renovation of sluices, surplus weirs under Rural Infrastructure Scheme (RIS) of the State Government.</li>
<li>Renovation of major ooranies and water bodies have been ordered vide G.O.(ID).No487 RD&PR (SGS-3) Department dated:18.09.2012.</li>
<li>Detailed Project Report (DPR) will be prepared by reputed agency and this will be executed during 2013-14.</li>
</ul>



		  

<b>    1.7 Proposed New initiatives in 2013-14</b>
<ul>
<li>The MGNREG Scheme will be implemented with the target for generating 42.85 crore mandays during 2013-14 with an approved Labour Budget of Rs.6341.80 crore.</li>
<li>The following new initiatives will be done during the implementation in 2013-14:</li>
<li>Exclusive staff strength for both implementing and monitoring MGNREGS works will be increased at all levels.</li>
