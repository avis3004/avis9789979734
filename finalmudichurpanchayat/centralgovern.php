</ul>
           
 <h2><a name="iay">	Indira Awaas Yojana (IAY)</a></h2>

<p>
 Indira Awaas Yojana, which used to be a component of Jawahar Rozgar Yojana became an independent Scheme from 1997-1998 onwards. Its objective is construction of free houses to members of the Scheduled Castes/ Scheduled Tribes, Freed Bonded Labourers in Rural areas and also to non SC/ST rural poor living below poverty line. The beneficiaries are selected from the Below Poverty Line (BPL) list approved by the Grama Sabha. Atleast 60% of the total IAY allocation during a financial year should be utilized for construction/ upgradation of dwelling units for SC/ST BPL rural households. A maximum 40% of allocation is for non- SC/ST BPL rural households. 3% of the above categories should be allocated for physically and mentally challenged persons. Government of India has also asked the States to earmark 15% of the beneficiaries from amongst minorities. It is funded by the Centre and State in the ratio of 75:25.</p>
 <p>         Upto 2011-12, the unit cost of the house constructed under Indira Awaas Yojana was Rs.45,000/-  Out of this, the Central share was Rs.33,750/- and the State&#146s share was Rs.11,250/-.  Due to considerable rise in the cost of construction materials, this Government has increased the State additional grant for RCC Roofing cost from Rs.30,000/- to Rs.55,000/- in G.O.No.87, RD&PR (CGS1) dated 16.11.2011.  Hence, during 2011-12, the unit cost of IAY house in Tamil Nadu was Rs.1 lakh of which the State Government alone has contributed Rs.66,250/- (66.25%) and the Centre&#146s Share is Rs. 33,750/- (33.75%).  During 2011-12, 1,00,553 houses were constructed at the cost of Rs.1005.53 crore.</p>
 <p>         For the year 2012-13, Rs.1114.10 crore has been allocated for the construction of 1,11,410 new houses under IAY. So far, 72,369 houses have been completed.  All the remaining houses are in progress at different stages and will be completed before the end of April 2013.</p>
 <p>         Now, Government of India has enhanced the unit cost of the house under Indira Awaas Yojana from Rs.45,000/- to Rs.70,000/- for the year 2013-14.  Still this unit cost of Rs.70,000/- is less than the unit cost of Rs.1 lakh adopted by Tamil Nadu. <b> During the financial year 2013-14, approximately 1 lakh houses will be constructed under Indira Awaas Yojana. </b></p>
 
 <h2><a name="mplads">Member of Parliament Local Area Development Scheme (MPLADS)</a></h2>
 <p>
 The objective of this scheme is to fill the gaps existing in the infrastructure facilities created through various Central and State Schemes thereby enabling wholesome development in both rural and urban areas. The Government of India has enhanced the MPLADS fund outlay from Rs.2.00 crore to Rs.5.00 crore per annum per MP from the financial year 2011-12.</p>
 
 <h2><a name="nirmal">Nirmal Bharat Abiyan</a></h2>
 <p>In order to ensure sanitation facilities in rural areas with the broader goal to eradicate the practice of open defecation, a comprehensive programme called &#146otal Sanitation Campaign&#146 was launched by Rajiv Gandhi National Drinking Water Mission, Government of India to cover all households with water and sanitation facilities and promote hygiene behaviour for overall improvement of health and sanitation in rural areas. The programme is demand-driven and people-centered based on the principle &#148From low to No subsidy&#148. A nominal subsidy in the form of incentive is given to the rural poor households for construction of toilets.  The key intervention areas have been identified as Individual Household Latrines (IHHL), School Sanitation & Hygiene Education, Community Sanitary Complex and Anganwadi Toilets. The Scheme laid strong emphasis on Information, Education and Communication (IEC), capacity building and hygiene education for effective behaviour change, with the involvement of PRIs, Community Based Organizations (CBOs), NGOs, etc. The Total Sanitation Campaign which was introduced in Cuddalore and Coimbatore districts initially in 1999 was extended in phases to all other districts in Tamil Nadu by 2004.  </p>
 <p>The Scheme has now been renamed as &#146Nirmal Bharat Abiyan&#146 (NBA) and revised guidelines have been issued by Government of India. The salient features of the revised guidelines are:</p>
 <ul>
 <li>Accelerating the sanitation coverage in the rural areas is advocated so as to comprehensively cover the rural community through saturation approach to create Nirmal Gram Panchayats.</li>
<li>Implementation of NBA is proposed with &#146Village Panchayat&#146 as the base unit. Village Panchayats where all habitations have access to water are to be taken 
up on priority.</li>
<li>Accelerate sanitation coverage in rural areas to achieve the vision of Nirmal Bharat by 2022 with all Village Panchayats in the Country attaining Nirmal status.</li>
<li>Incentive as provided under the scheme for construction of Individual Household Latrines (IHHL) has been extended to all Below Poverty Line (BPL) Households and Above Poverty Line Households (APL) restricted to SCs/STs, Small and Marginal farmers, Landless Labourers with homestead, Differently Abled and Women Headed households.</li>
<li>The incentive amount for construction of one unit of IHHL has been increased from Rs.3,200 to Rs.4600 (Rs.5100 for difficult and hilly areas). The Central share out of this is Rs.3200 (Rs.3700 in case of hilly and difficult areas) and State Government share is Rs.1400.  Minimum beneficiary share shall be Rs.900. </li>
<li>The schools which are not yet covered under SSA and Anganwadi Centres in the rural areas will be provided with proper sanitation facilities and proactive promotion of hygiene education and sanitary habits among students will be undertaken.</li>
<li>Appropriate convergence with MGNREGS has been provided.</li>
<li>All houses constructed by the beneficiaries  under Indira Awas Yojana (IAY) or any other State rural housing Scheme which did not have toilets shall also be eligible for the incentive for creation of  sanitation facilities.</li>
<li>Solid and Liquid Waste Management (SLWM) may be taken in the proposed and existing Nirmal Gram Panchayats. The total assistance under NBA for SLWM projects shall be worked out on the basis of total number of households in each Village Panchayat, subject to a maximum of:</li>
<ol type="i">
<li>Rs.7  lakh upto 150 households.</li>
<li>Rs.12 lakh upto 300 households.</li>
<li>Rs.15 lakh upto 500 households and</li>
<li>Rs.20 lakh for a Village Panchayat having more than 500 Households</li>
</ol></ul>

 <h2><a name="pmgsy">
Pradhan Mantri Gram Sadak Yojana (PMGSY)
</a></h2>
<p>Pradhan Mantri Gram Sadak Yojana was launched in 2000 to provide all weather road access to unconnected habitations. PMGSY is a 100% centrally sponsored scheme. The programme objective is to provide round-the-year connectivity by all weather roads with necessary culverts and cross drainage structures. Between PMGSY Phase - I (2000-01) and PMGSY Phase - VII (2007-08) i.e Bharat Nirman Phase - II, 4,946 roads have been taken up for a length of 9,970.150 Km at a cost of Rs.2,022 crore. All the works have been completed. </p>
<p>In Tamil Nadu, all habitations with more than 1000 population have been provided connectivity except for 1 habitation in Dharmapuri District. Now, Detailed Project Report is being prepared for providing connectivity to the above habitation through Forest Department.</p>
<b>8.1 New Proposals sanctioned under PMGSY Phase VIII (2012-13)
<p>        After a gap of 4 years, due to the sustained efforts of the State Government, the Government of India, Ministry of Rural Development has recently sanctioned proposals under PMGSY Phase-VIII as detailed below:</p></b>
<b>8.1.1 New Connectivity (500-999 category)</b>
        <p>40 road works providing connectivity to 41 unconnected habitations for a length of 90.03 Km, at a total cost of Rs.39.01 crore have been sanctioned.</p>
<b>8.1.2 Upgradation </b>
         <p> Based on the pavement condition, upgradation of 1258 roads for a length of 3005.74 Km at a total cost of Rs.1012.910 crore has been taken up.</p>
<b>8.1.3 Roads using Coir Geo Textiles </b>
<p>As an innovative technological intervention, the Government of India has accorded approval under PMGSY for 19 road works using Coir-Geo Textiles Technology for a length of 42.20 Km at a cost of Rs.16.74 crore.  </p>
<b>8.1.4 Roads using RBI Grade 81    </b>
       <p> On a pilot basis, 7 roads are to be laid using RBI Grade 81, which is a soil stabilizing chemical to improve the strength of the soil. Road works for a length 16.50 km at a cost of Rs. 5.71 crore will be taken up shortly.</p>
<b>8.1.5 Long Span Bridges</b>
        <p>In order to ensure uninterrupted connectivity throughout the year, 45 long span bridges at a total cost of Rs.78.18 crore has been taken up. </p>
<p><b>
        Totally 1,343 road and bridge works for a length of 3095.77 Km at a cost Rs.1130.10 crore have been taken up. This includes Rs.108.95 crore as State share.
