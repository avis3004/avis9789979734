            <h2><a name="thai">TamilNadu Village Habitation Improvement Scheme (THAI)</a></h2>
			<p align="justify">
			This Government has introduced the flagship programme called Tamil Nadu Village Habitations Improvement (THAI) Scheme from 2011-12 onwards to overcome 
			the bottlenecks in the uneven distribution of resources and to provide minimum basic infrastructure facilities to all the habitations. <b>Tamil Nadu is the 
			only State focusing on Habitation as the unit of development and no other State in the Country is implementing such an innovative scheme.</b></p>
			<p align="justify">
			There are about 7 habitations on an average for each Village Panchayat in Tamil Nadu with 79,394 habitations in 12,524 Village Panchayats. However, the number of 			            habitations varies from District to district. The average number of habitations in a Village Panchayat in The Nilgiris District is 37 whereas it is only 3 in Villupuram            District. Therefore, allocation of equal amount to all the Village Panchayats, with varied number of habitations, has led to disparity in development, disproportionate            distribution of assets and uneven progress. The THAI scheme has come as boon with the aim of extending the basic facilities to the grass root habitation.</p>
