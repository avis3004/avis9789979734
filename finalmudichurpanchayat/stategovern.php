<h2><b><a name="cmsghs">Chief Minister Solar Powered Green House Scheme (CMSPGHS)</a></b></h2>
<p> <i>&#148This Government will launch a Solar Powered Green House Scheme for the benefit of the poor in the rural areas and construct houses measuring about 300 square feet at a unit cost of Rs.2.10 lakh by meeting the entire cost by the State  Government. </i></p>
<p>Accordingly this Government had ordered for construction of 60,000 Solar Powered Green Houses of 300 square feet each, every year for the next five years commencing from 2013-14 at a unit cost of Rs.2.10 lakhs per house.  The scheme is named as Chief Ministers Solar Powered Green House Scheme (CMSPGHS)&#148.</p>
<p><b>Construction of houses under &#148Chief Ministers Solar Powered Green House Scheme&#148 ordered by the Honourable &#148Chief Minister&#146s is another milestone in the history of rural housing which clearly addresses the  shelter needs in rural areas. Nowhere in the country is such a free housing scheme for the construction of houses with an area of 300 sq.ft </b></p>
<p><b>Salient features of the Scheme</p></b>
<ul>
<li>	All the people living below poverty line in rural areas are eligible for Solar Powered Green Houses.</li>
<li>	Each house is built with an area of 300 square feet at a unit cost of Rs.2.10 lakh fully funded by the State Government.</li>
<li>Each house consists of a living room, bed room, kitchen, toilet and verandah apart from Rain Water Harvesting provision.</li>
<li>Each house is provided with 5 solar powered Compact Fluorescent Lamps (CFL), one each in bed room, living room, kitchen, toilet and verandah. 
 Each beneficiary is given the option to have an electric connection powered by TNEB, which will be metered.</li> 
 <li>The unit cost is Rs.2.10 lakh per house @Rs.1.80 lakh for construction and Rs.30,000/- for Solar Powered Lights.</li>
<li>The RD & PR Department is entrusted with the construction of houses and Tamil Nadu Energy Development Agency (TEDA) is the implementing agency for solar lighting component.</li>
<li>The Green Houses are constructed either insitu (replacing his/her existing dwelling structure) or in the land owned by the beneficiary elsewhere in the Village Panchayat.  No land acquisition is envisaged under this scheme.  Only people with patta for their sites are eligible under this scheme.</li>
<li>The supply, installation and commissioning of solar panels and lights are undertaken directly by TEDA by following established procedures in co-ordination with the Project Directors of District Rural Development Agencies.</li></ul>
<p><b>Type design and unit cost</b></p>
<ul>
<li>Each house shall be constructed with a maximum plinth area of 300 sq.ft at a unit cost of Rs2.10 lakh.  The houses will be built with a living room, bed room, kitchen and a toilet.  Wherever possible, the toilet shall be constructed by dovetailing funds from the Total Sanitation Campaign.  Rain Water Harvesting Structure shall be provided in all the houses.  The Solar powered lighting system shall be installed by TEDA in co-ordination with the District officials.</li>
<li>An exclusive type design for this Scheme has been developed and the construction of houses should not exceed the permissible limit of 300 sq.ft.  To ensure uniformity in the design, no change of type design is normally permitted.</li>
