<p align="justify">The requirement of a Village Panchayat will be more if it has more number of habitations.  Taking this concept into consideration, the Village Panchayats have been grouped into 4 categories based on the number of habitations for the purpose of allocation of funds.  Thus, Village Panchayats with more number of habitations and having a larger population shall receive more funds.  The above table clearly depicts the fund requirement for all the 12,524 Village Panchayats. Thus the habitations in all the Village Panchayats in Tamil Nadu would be covered at a total cost of Rs.3,400 crores under the THAI scheme. In 2011-12, the scheme has been implemented with an annual allocation of Rs.680 crore. Considering the importance of filling up the infrastructural gaps of rural habitations, the Government has allocated Rs. 750  crore during 2012-13.</p>
