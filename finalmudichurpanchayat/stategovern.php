<p align="left"><b> </b></p>

 <p align="left"><b> Water Supply</b></p>
   <p align="justify">        Drinking water supply works like provision of power pump, extension of pipeline, augmentation of existing source shall be taken up depending upon the requirement.</p>

 <p align="left"><b> Street Lights</b></p>
        <p align="justify">    New streetlights will be provided in the new habitations, extension areas, Adi-Dravidar & Tribal habitations and IAY housing colonies, based on the actual needs. Additional street lights, including solar powered lights will be provided in the areas where the existing coverage is felt inadequate.</p>
 <p align="left"><b> Cement Concrete Roads</b></p> 
          <p align="justify">   Pavements with residential units on sides, streets or lanes prone to water logging, part of the uncovered portion where a portion of the street has already been covered with cement concrete, will be taken up on priority. </p>
 <p align="left"><b> Link Roads</b></p>
         <p align="justify">   Link roads will cover bus-plying roads, upgradation of WBM roads to BT roads connecting two or more habitations in the same Panchayat and upgradation of earthen/gravel roads.
 <p align="left"><b> Cremation / Burial Grounds</b></p>
<p align="justify">  Each burial ground / cremation ground will be provided with approach road, compound wall, cremation shed, water facility and lights. 
<p align="left"><b>  Pathway to Burial Ground</b></p>
<p align="justify">  Wherever the pathway is already available, improvement of the existing pathway will be taken up and wherever pathway is not available, top priority will be given to provide approach road or pathway.</p>
     <p align="justify">  The additional requirements and other permitted works will be taken up based on the need and necessity. Apart from THAI, the scheme will be implemented by dovetailing funds, from various schemes of Rural Development & Panchayat Raj Department and schemes of other Departments.</p>
